    private void jbtnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConvertActionPerformed
        // TODO add your handling code here:
        double dst1 = Double.parseDouble (jtxtEnter1.getText());
        double dst2 = Double.parseDouble (jtxtEnter2.getText());
        double dst3;
        
        if ((jtxtEnter1.getText().equals("")) || (jtxtEnter2.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null, "Anda harus memasukkan nilai untuk dihitung",
                    "Pemberitahuan!", JOptionPane. INFORMATION_MESSAGE);
        }
        
        else if (jCJarak.getSelectedItem().equals("Jarak"))
        {
            //Jarak = kecepatan * waktu
            jlblDisplay1.setText ("       Jarak = ");
            jlblDisplay2.setText("Kecepatan   X");
            jlblDisplay3.setText("Waktu");
            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            jlblResult.setText (value);
        }
        
        else if (jCJarak.getSelectedItem().equals("Kecepatan"))
        {
            //Jarak = kecepatan * waktu
            jlblDisplay1.setText ("   Kecepatan = ");
            jlblDisplay2.setText("        Jarak   /");
            jlblDisplay3.setText("Waktu");
            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            jlblResult.setText (value);
        }
        
        else if (jCJarak.getSelectedItem().equals("Waktu"))
        {
            //Jarak = kecepatan * waktu
            jlblDisplay1.setText ("     Waktu = ");
            jlblDisplay2.setText("        Jarak   /");
            jlblDisplay3.setText("Kecepatan");
            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            jlblResult.setText (value);
        }
    }//GEN-LAST:event_jbtnConvertActionPerformed

