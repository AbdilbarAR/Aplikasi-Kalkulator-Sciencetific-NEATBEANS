    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    try {
    String input = input_bilangan.getText();
    String selectedFrom = (String) bilangan_dari.getSelectedItem();
    String selectedTo = (String) bilangan_ke.getSelectedItem();

    if (selectedFrom.equals("Hexadecimal")) {
        int decimal = Integer.parseInt(input, 16);
        jlblDecimal.setText(Integer.toString(decimal));

        if (selectedTo.equals("Decimal")) {
            output_bilangan.setText(Integer.toString(decimal));
        } else if (selectedTo.equals("Biner")) {
            output_bilangan.setText(Integer.toBinaryString(decimal));
        } else if (selectedTo.equals("Octal")) {
            output_bilangan.setText(Integer.toOctalString(decimal));
        } else {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Memilih ComboBox Yang Sama!");
        }
    } else if (selectedFrom.equals("Octal")) {
        int decimal = Integer.parseInt(input, 8);
        jlblDecimal.setText(Integer.toString(decimal));

        if (selectedTo.equals("Decimal")) {
            output_bilangan.setText(Integer.toString(decimal));
        } else if (selectedTo.equals("Biner")) {
            output_bilangan.setText(Integer.toBinaryString(decimal));
        } else if (selectedTo.equals("Hexadecimal")) {
            output_bilangan.setText(Integer.toHexString(decimal));
        } else {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Memilih ComboBox Yang Sama!");
        }
    } else if (selectedFrom.equals("Biner")) {
        int decimal = Integer.parseInt(input, 2);
        jlblDecimal.setText(Integer.toString(decimal));

        if (selectedTo.equals("Decimal")) {
            output_bilangan.setText(Integer.toString(decimal));
        } else if (selectedTo.equals("Octal")) {
            output_bilangan.setText(Integer.toOctalString(decimal));
        } else if (selectedTo.equals("Hexadecimal")) {
            output_bilangan.setText(Integer.toHexString(decimal));
        } else {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Memilih ComboBox Yang Sama!");
        }
    } else if (selectedFrom.equals("Decimal")) {
        int decimal = Integer.parseInt(input);
        jlblDecimal.setText(Integer.toString(decimal));

        if (selectedTo.equals("Octal")) {
            output_bilangan.setText(Integer.toOctalString(decimal));
        } else if (selectedTo.equals("Biner")) {
            output_bilangan.setText(Integer.toBinaryString(decimal));
        } else if (selectedTo.equals("Hexadecimal")) {
            output_bilangan.setText(Integer.toHexString(decimal));
        } else {
            JOptionPane.showMessageDialog(null, "Tidak Boleh Memilih ComboBox Yang Sama!");
        }
    }
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(null, "Input Tidak Benar");
}
    }//GEN-LAST:event_jButton1ActionPerformed

